class Scooter {
    constructor(year, color, model) {
        this.model = model;
        this.year = year;
        this.color = color;
    }
}

class Driver {
    constructor(name, age, experience) {
        this.name = name;
        this.age = age;
        this.experience = experience;
    }
}

class PickupLocation {
    constructor(address, city) {
        this.city = city;
        this.address = address;
    }
}